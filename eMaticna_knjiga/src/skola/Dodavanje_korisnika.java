public void korisnici()
    {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/ematicna_baza","root",""); 
             
            pst = con.prepareStatement("select * from korisnik");
            ResultSet Rs = pst.executeQuery();
            
            ResultSetMetaData rd = Rs.getMetaData();
            int c = rd.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)jTable_tablica.getModel();
            df.setRowCount(0);
            
             while(Rs.next())
            {
               Vector v2 = new Vector();
               
               for(int i=1; i<c; i++)
               {
                   
                   v2.add(Rs.getString("id"));
                   v2.add(Rs.getString("korisnicko_ime"));
                   v2.add(Rs.getString("lozinka"));
                   v2.add(Rs.getString("tip_korisnika"));
                 
               }
               df.addRow(v2);
            }
                    
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Dodavanje_korisnika.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Dodavanje_korisnika.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }
