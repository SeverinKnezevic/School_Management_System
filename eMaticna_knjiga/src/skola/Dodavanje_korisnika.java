    private void jButton_Dodaj_korisnika_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Dodaj_korisnika_ActionPerformed
        // TODO add your handling code here:

        if(txtKorisnicko_ime.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Unesite Vaše korisničko ime");
        }
        else if(txtLozinka.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Unesite Vašu lozinku");
        }
        else if(txtLozinka.getText().equals(txtPonovi_lozinku.getText()) == false)
        {
            JOptionPane.showMessageDialog(this,"Lozinka nije ispravna");
        }

        else
        {
            try {

                String korisnicko_ime = txtKorisnicko_ime.getText();
                String lozinka = txtPonovi_lozinku.getText();
                String tip_korisnika = txtUloga.getSelectedItem().toString();

                Class.forName("com.mysql.jdbc.Driver");

                con = DriverManager.getConnection("jdbc:mysql://localhost/ematicna_baza","root","");
                pst = con.prepareStatement("insert into korisnik(korisnicko_ime,lozinka,tip_korisnika)values(?,?,?)");
                pst.setString(1, korisnicko_ime);
                pst.setString(2, lozinka);
                pst.setString(3, tip_korisnika);

                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Novi korisnik je registriran");
                txtKorisnicko_ime.setText("");
                txtLozinka.setText("");
                txtPonovi_lozinku.setText("");
                txtUloga.setSelectedIndex(-1);
                txtKorisnicko_ime.requestFocus();

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Dodavanje_korisnika.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Dodavanje_korisnika.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        this.hide();
        Dodavanje_korisnika d_k = new Dodavanje_korisnika();
        d_k.setVisible(true);
    }//GEN-LAST:event_jButton_Dodaj_korisnika_ActionPerformed

